export function getAll(name) {
  try {
    checkVersion();
    const data = localStorage.getItem(getKey(name));
    if (data) {
      const parsed = JSON.parse(data);
      if (name === "blog" && Array.isArray(parsed)) {
        const defaultsMap = {};
        (defaults.blog || []).forEach((d) => { defaultsMap[d.id] = d; });
        const migrated = parsed.map((item) => {
          const def = defaultsMap[item.id];
          if (!def) return item;
          const merged = { ...def, ...item, image: def.image || item.image };
          return merged;
        });
        setAll(name, migrated);
        return migrated;
      }
      return parsed;
    }
    if (defaults[name]) {
      setAll(name, defaults[name]);
      return defaults[name];
    }
    return null;
  } catch {
    return null;
  }
}

export function setAll(name, items) {
  localStorage.setItem(getKey(name), JSON.stringify(items));
}

export function getById(name, id) {
  const items = getAll(name);
  return items ? items.find((i) => i.id === id) : null;
}

export function addItem(name, item) {
  const items = getAll(name) || [];
  const newItem = { ...item, id: Date.now().toString() };
  items.push(newItem);
  setAll(name, items);
  return newItem;
}

export function updateItem(name, id, updates) {
  const items = getAll(name) || [];
  const idx = items.findIndex((i) => i.id === id);
  if (idx === -1) return null;
  items[idx] = { ...items[idx], ...updates };
  setAll(name, items);
  return items[idx];
}

export function deleteItem(name, id) {
  const items = getAll(name) || [];
  const filtered = items.filter((i) => i.id !== id);
  if (filtered.length === items.length) return false;
  setAll(name, filtered);
  return true;
}

export function seedData(name, data) {
  const existing = localStorage.getItem(getKey(name));
  if (!existing) {
    setAll(name, data);
  }
}
